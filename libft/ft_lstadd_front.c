#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!new || !lst)
		return ;
	new->next = *lst;
	*lst = new;
}
//youre saying the new node is referencing the first node in the list.
// ANd youre making the list point to it as a start instead of the old start