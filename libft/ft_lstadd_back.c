#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	last = ft_lstlast(*lst);
	if (!last)
	{
		*lst = new;
	}
	last->next = new;
}
//if last node is not null, point to it then initiate it,
// if it's null initiate it directly