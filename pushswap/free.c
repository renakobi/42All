#include "push_swap.h"

void	free_stack(t_stack **stack)
{
	t_stack	*tmp;

	while (*stack)
	{
		tmp = (*stack)->next;
		free(*stack);
		*stack = tmp;
	}
}

void	free_both(t_stack **a, t_stack **b)
{
	free_stack(a);
	free_stack(b);
}
